<script type="text/javascript">
    $(function() {
        var count = 0;
        var body = $('body');
        $(window).scroll(function() {
            if (($(window).height() + $(window).scrollTop() + 400) >= body.height()) {
                if (count < 2) {
                    count++;
                    if ($('.item-lazyload-step' + count).length > 0) {
                        $('.item-lazyload-step' + count).show(500);
                    }
                    body = $('body');
                }
            }
        })
    });
</script>