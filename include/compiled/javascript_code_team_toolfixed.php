<script type="text/javascript">
    $(window).scroll(function() {
        var scroll = $(window).scrollTop();
        var $nav = $(".item-details-content nav");
        var contentHeight = $(".item-details-content").offset().top;
        var positionHeight;
        if ($("#position").length > 0) {
            positionHeight = $("#position").offset().top;
        }
        var informationHeight;
        if ($("#information").length > 0) {
            informationHeight = $("#information").offset().top;
        }
        var introduceHeight;
        if ($("#introduce").length > 0) {
            introduceHeight = $("#introduce").offset().top;
        }
        var discussHeight = $("#discuss").offset().top;
        if (scroll >= contentHeight) {
            $nav.addClass('scroll');
            $('.detailsNav-1').addClass('active').siblings().removeClass('active');
            $('#Totop,#buyNow').show();
            $nav.css('border-bottom', '2px solid #fe9c50');
        }
        if (scroll < contentHeight) {
            $nav.removeClass('scroll');
            $('.detailsNav-1').removeClass('active');
            $('#Totop,#buyNow').hide();
            $nav.css('border-bottom', '1px solid #d4d4d4');
            $('#position').css('padding-top', '20px');
        }
        if (scroll >= informationHeight) {
            $('.detailsNav-2').addClass('active').siblings().removeClass('active');
        }
        if (scroll < informationHeight) {
            $('#information').css('padding-top', '20px');
        }
        if (scroll >= introduceHeight) {
            $('.detailsNav-3').addClass('active').siblings().removeClass('active');
        }
        if (scroll < introduceHeight) {
            $('#introduce').css('padding-top', '20px');
        }
        if (scroll >= discussHeight) {
            $('.detailsNav-4').addClass('active').siblings().removeClass('active');
        }
        if (scroll < discussHeight) {
            $('#discuss').css('padding-top', '20px');
        }
    });
    $(function() {
        $("#Totop").click(function() {
            $('body,html').animate({scrollTop: 0}, 400);
            return false;
        });
        $('.detailsNav-1 a').click(function() {
            $('#position').css('padding-top', '56px');
        });
        $('.detailsNav-2 a').click(function() {
            $('#information').css('padding-top', '56px');
        });
        $('.detailsNav-3 a').click(function() {
            $('#introduce').css('padding-top', '56px');
        });
        $('.detailsNav-4 a').click(function() {
            $('#discuss').css('padding-top', '56px');
        });
    });
</script>